// ✅ 2xx - Successful Responses
// Indicates that the client’s request was successfully processed.
//
//     200 OK: The request was successful (most common code).
//     201 Created: A new resource has been created (usually with POST requests).
//     202 Accepted: The request has been accepted but not yet processed.
//     203 Non-Authoritative Information: The response is not from the original source.
//     204 No Content: The request was successful, but there is no content to return.
//     205 Reset Content: The client should reset the view (e.g., clear a form).
//     206 Partial Content: Part of the content has been delivered (used for segmented downloads).
//     207 Multi-Status: Multiple status responses for batch operations (used in WebDAV).
//     208 Already Reported: The information has already been reported (used in WebDAV).
//     226 IM Used: The response uses delta encoding for efficient updates.






// ✅ 2xx - موفقیت‌آمیز (Successful Responses)
// نشان‌دهنده این است که درخواست کلاینت با موفقیت پردازش شده است.
//
//     200 OK: درخواست با موفقیت انجام شد (رایج‌ترین کد).
//     201 Created: منبع جدیدی ایجاد شده است (معمولاً در POST).
//     202 Accepted: درخواست پذیرفته شده، اما هنوز پردازش نشده است.
//     203 Non-Authoritative Information: پاسخ از منبع اصلی نیست.
//     204 No Content: درخواست موفق بود اما پاسخی ندارد.
//     205 Reset Content: باید نمای کاربر ریست شود (مثلاً پاک کردن فرم).
//     206 Partial Content: بخشی از محتوا ارسال شده (مناسب برای دانلود بخش‌بندی‌شده).
//     207 Multi-Status: وضعیت‌های مختلف برای عملیات چندگانه (در WebDAV).
//     208 Already Reported: اطلاعات قبلاً گزارش شده است (در WebDAV).
//     226 IM Used: پاسخ از قابلیت‌های تغییرات داخلی استفاده کرده است.
