// ✅ 5xx - Server Errors
// Indicates server-side errors.
//
//     500 Internal Server Error: A generic error indicating an unexpected condition on the server.
//     501 Not Implemented: The server doesn’t support the functionality required to fulfill the request.
//     502 Bad Gateway: The server received an invalid response from an upstream server.
//     503 Service Unavailable: The server is temporarily unavailable (e.g., for maintenance).
//     504 Gateway Timeout: The server didn’t receive a timely response from an upstream server.
//     505 HTTP Version Not Supported: The server doesn’t support the HTTP version used in the request.
//     506 Variant Also Negotiates: Content negotiation resulted in an error.
//     507 Insufficient Storage: The server is unable to store the representation needed (used in WebDAV).
//     508 Loop Detected: An infinite loop was detected while processing the request.
//     510 Not Extended: Further extensions are required to fulfill the request.
//     511 Network Authentication Required: Authentication is required to access the network



// ✅ 5xx - خطاهای سمت سرور (Server Errors)
// نشان‌دهنده خطاهای داخلی سرور است.
//
//     500 Internal Server Error: خطای داخلی سرور (عمومی‌ترین خطای سرور).
//     501 Not Implemented: سرور از این قابلیت پشتیبانی نمی‌کند.
//     502 Bad Gateway: سرور از یک Gateway دیگر پاسخ نامعتبر دریافت کرده است.
//     503 Service Unavailable: سرور موقتاً در دسترس نیست (نگهداری یا بار زیاد).
//     504 Gateway Timeout: سرور از Gateway دیگر پاسخی دریافت نکرده است.
//     505 HTTP Version Not Supported: نسخه HTTP پشتیبانی نمی‌شود.
//     506 Variant Also Negotiates: خطا در مذاکره محتوا (Content Negotiation).
//     507 Insufficient Storage: فضای ذخیره‌سازی کافی نیست (در WebDAV).
//     508 Loop Detected: حلقه بی‌پایان در پردازش درخواست شناسایی شده است.
//     510 Not Extended: نیاز به افزونه‌های بیشتر برای پردازش درخواست.
//     511 Network Authentication Required: نیاز به احراز هویت برای دسترسی به شبکه.
