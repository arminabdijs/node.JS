//Shorthands help us write shorter and more useful regex


// \d can be used for shorthand numbers
//\d===[0-9]
//         /\d{4}/g

// \D !==[0-9]
//         /\D{4}/g


// \w can be used for shorthand letters
// \w ===[a-zA-Z0-9]
//         /\w{4}/g

// \W !==[a-zA-Z0-9]
//         /\W{4}/g


// \s can be used for shorthand whitespace
//         /\s{4}/g 


// \S !==whitespace

// \t can be used for shorthand tabs
//         /\t{4}/g

// To find a letter at the end of words, we use Shorthand \b
//           /ed\b/g



// To find a letter at the start of words, we use Shorthand \B
//           /ed\B/g
